<div class = "hello">The term “noise temperature” is generally used to present how much thermal noise is generated from active or passive devices in the receiving system. The noise power Pn is written as noise temperature as follows:

Pn = kTnB
Where,
k = Bolzmann's constant = 1.38 × 10−23 J/K
   = −228.6 dBW/K/Hz
Tn = noise temperature (K)
B = bandwidth (Hz).
Formula for noise temperature is,  Noise Temperature = (F-1)*To = (F-1)*300.
A noise figure of 3dB, translates to an equivalent noise temperature equal to the room temperature and the relation between 
noise figure and noise temperature is, Noise Temperature is directly proportional to Noise Figure.</br></br>
The noise temperature of the second stage is divided by the power gain of the first stage when referred to the input. Therefore, in order to keep the overall system noise as low as possible, the first stage (usually as LNA) should have high power gain as well as noise temperature.</br></br>
Advantage of the use of noise temperature for low noise levels is that it shows a greater variation for any given noise-level change than does the noise figure, so changes are easier to grasp in their true perspective.


 
It will be recalled that the equivalent noise resistance introduced in Section 2-3 is quite fictitious, but it is often employed because of its convenience. </div>
	


