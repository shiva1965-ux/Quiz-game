<div class = "hello"> Noise figure and noise factor are measures of degradation of the signal-to-noise ratio, caused by components in a signal chain. It is a number by which the performance of an amplifier or a radio receiver can be specified, with lower values indicating better performance.
Typical noise figures for practical receivers are in the range of ~2 to 10dB depending on power, supply voltage, process and circuit design. A receiver whose spot NF is 3 dB has an input noise power, Ni that is equivalent to available noise power of the source, or kT.</br>
or</br>
Noise Figure is defined as the ratio of signal to noise ratio at the output to that at the input. In other words,

NF= (s/n)i/(s/n)o

Where (s/n)I is the signal to noise ratio at the input, and (s/n)o is the signal to noise ratio at the output of the device under test. Note that s/n at the output will always be smaller than the s/n at the input, due to the fact that any circuit will only add to the noise, but never reduces the noise present in the system.
